#include "libft.h"

void	ft_binar(int n)
{
	int i;

	i = 128;
	if (n < 255)
	{
		while (i)
		{
			if ((n / i) > 0)
				ft_putchar('1');
			else
				ft_putchar('0');
			n %= i;
			i /= 2;
		}
	}
}
