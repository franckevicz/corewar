char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int y;

	i = 0;
	while (s1[i] != '\0')
		i++;
	y = 0;
	while (s2[y] != '\0')
	{
		s1[i] = s2[y];
		i++;
		y++;
	}
	s1[i] = '\0';
	return (s1);
}
